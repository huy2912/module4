    public void ClearData(){
        int n = tableModel.getRowCount()-1;
        for(int i = n; i >= 0; i--){
            tableModel.removeRow(i);
        }
    }

    public void ShowData(){
        ResultSet data = conn.LoadData("select * from NhanVien");
        try{
            while(data.next()){
                String[] row = new String[6];
                row[0] = data.getString(1);
                row[1] = data.getString(2);
                row[2] = data.getString(3);
                row[3] = data.getString(4);
                row[4] = data.getString(5);
                row[5] = data.getString(6);
                tableModel.addRow(row);
            }
        }catch(Exception e){

        }
    }

