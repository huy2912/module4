    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        TableModel tm = jTable1.getModel();
        maTxt.setText(tm.getValueAt(i, 0).toString());
        tenTxt.setText(tm.getValueAt(i, 1).toString());
        birthTxt.setText(tm.getValueAt(i, 2).toString());
        diaChiTxt.setText(tm.getValueAt(i, 3).toString());
        sdtTxt.setText(tm.getValueAt(i, 4).toString());
        String sex = tm.getValueAt(i, 5).toString();
        namR.setSelected(sex=="nam"?true:false);
        nuR.setSelected(sex=="nu"?true:false);
    }//GEN-LAST:event_jTable1MouseClicked

